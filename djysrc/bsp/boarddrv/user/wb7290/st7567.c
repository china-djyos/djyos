static void set_lcd_dat(u8 dat)
{
    Pio *pio;
    u32 bak;

    pio=PIOC;
    bak=pio->PIO_OWER;;

    pio->PIO_OWER |=0x000000FF; //只写PC[7:0]
    pio->PIO_ODSR =dat;

    pio->PIO_OWER =bak;

}

static u8 get_lcd_dat(void)
{
    Pin pin;
    u8 dat;
    Pio *pio;

    pio =PIOC;

    //设置为输入模式．
    pin.mask =0x0000000FF;//PC[7:0]
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_INPUT;
    pin.attribute =PIO_OPENDRAIN;
    PIO_Configure(&pin,1);

    pio->PIO_PER |= 0x000000FF;
    dat =pio->PIO_PDSR;

    //设置回输出模式．
    pin.mask =0x000000FF; //PC[7:0]
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_DEFAULT;
    PIO_Configure(&pin,1);
    return dat;

}
static void Lcd_WriteCommand(u8 Cmd)
{
    set_lcd_dat(Cmd);
    LCD_RS_0();
    LCD_CS_0();
    LCD_RD_1();
    LCD_WR_0();

    Djy_DelayUs(1);     //needed
    LCD_WR_1();
    LCD_CS_1();
}

static void Lcd_WritdData(u8 Data)
{
    set_lcd_dat(Data);
    LCD_RS_1();
    LCD_CS_0();
    LCD_RD_1();
    LCD_WR_0();

    Djy_DelayUs(1);//needed
    LCD_WR_1();
    LCD_CS_1();
}

void Lcd_WriteSpecialCmd(u8 Cmd)
{
    set_lcd_dat(Cmd);
    LCD_RS_0();
    LCD_CS_0();
    LCD_RD_0();
    LCD_WR_0();

    Djy_DelayUs(1);     //needed
    LCD_WR_1();
    LCD_CS_1();
}

static u8 Data_Get(void)
{
    u8 data;

    LCD_RS_1();
    LCD_CS_0();
    LCD_WR_1();
    LCD_RD_0();

    data = get_lcd_dat();
    LCD_RD_1();
    LCD_CS_1();

    return data;
}

void Lcd_PortInit(void)
{
    Pin pin;
    Pio *pio=PIOC;

    PMC_EnablePeripheral(ID_PIOA);
    PMC_EnablePeripheral(ID_PIOB);
    PMC_EnablePeripheral(ID_PIOC);
    PMC_EnablePeripheral(ID_PIOD);

    ////LCD BackLight: PD23,0:ON,1:OFF
    pin.mask =PIO_PD23;
    pin.pio  =PIOD;
    pin.id   =ID_PIOD;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);
    PIO_Clear(&pin);  //Backlight ON.
    PIO_Set(&pin);

    ////LCD CS: PIO_PC14
    pin.mask =PIO_PC14;
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);

    ////LCD RS: PIO_PC18
    pin.mask =PIO_PC18;
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);

    ////LCD WR: PC8
    pin.mask =PIO_PC8;
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);

    ////LCD RD: PC11
    pin.mask =PIO_PC11;
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_1;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);

    ////LCD DAT: PC[7:0]
    pin.mask =LCD_D0_D7;
    pin.pio  =PIOC;
    pin.id   =ID_PIOC;
    pin.type =PIO_OUTPUT_0;
    pin.attribute =PIO_PULLUP;
    PIO_Configure(&pin,1);

    pio =PIOC;
    pio->PIO_OER   =0xFF;
}

