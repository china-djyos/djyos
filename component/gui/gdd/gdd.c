static  struct MutexLCB *gdd_mutex_lock=NULL;

bool_t    __GDD_Lock(void)
{
    Lock_MutexPend(gdd_mutex_lock, CN_TIMEOUT_FOREVER);
    return TRUE;
}

void    __GDD_Unlock(void)
{
    Lock_MutexPost(gdd_mutex_lock);
}

