    WriteData(0x47);
    WriteData(0x06);
    WriteData(0x0a);
    WriteData(0x07);
    WriteData(0x30);
    WriteData(0x37);
    WriteData(0x0f);

    WriteComm(0XC0); //Power Control 1
    WriteData(0x18); //Vreg1out
    WriteData(0x17); //Verg2out

    WriteComm(0xC1); //Power Control 2
    WriteData(0x41); //VGH,VGL

    WriteComm(0xC5); //Power Control 3
    WriteData(0x00);
    WriteData(0x1A); //Vcom
